// ── Edge cases: special characters ─────────────────────────────────────────

describe('mentalHealthToMarkdown — special characters', () => {
  it('preserves special chars (& < > ") in gene descriptions', () => {
    const sections: PathwaySection[] = [{
      narrative: {
        pathway: 'Test',
        status: 'actionable',
        body: 'Body with & < > "quotes".',
        priority: '1 actionable',
        hint: '',
        geneCount: 1,
        actionCount: 0,
      },
      genes: [{
        symbol: 'GENE1', variant: 'A>T', rsid: 'rs123',
        genotype: 'A/T', status: 'actionable', evidenceTier: 'E2',
        studyCount: 1, description: 'Risk & benefit "balanced" <high>.',
        actionCount: 0, categories: ['mood'], pathway: 'Test',
      }],
    }]

    const md = mentalHealthToMarkdown(sections, {})
    expect(md).toContain('Body with & < > "quotes".')
    expect(md).toContain('Risk & benefit "balanced" <high>.')
    expect(md).toContain('A>T')
  })
})

describe('mentalHealthToMarkdown — empty actions list', () => {
  it('skips actions section when gene has no actions', () => {
    const sections: PathwaySection[] = [{
      narrative: {
        pathway: 'Empty',
        status: 'monitor',
        body: 'Nothing to do.',
        priority: '2 monitor',
        hint: '',
        geneCount: 1,
        actionCount: 0,
      },
      genes: [{
        symbol: 'COMT', variant: 'Val158Met', rsid: 'rs4680',
        genotype: 'A/G', status: 'monitor', evidenceTier: 'E3',
        studyCount: 5, description: 'Intermediate.',
        actionCount: 0, categories: ['mood'], pathway: 'Empty',
      }],
    }]

    const md = mentalHealthToMarkdown(sections, {})
    expect(md).not.toContain('### Actions for COMT')
  })

  it('skips actions section when actions record has empty array', () => {
    const sections: PathwaySection[] = [{
      narrative: {
        pathway: 'Empty',
        status: 'monitor',
        body: 'Nothing.',
        priority: '2 monitor',
        hint: '',
        geneCount: 1,
        actionCount: 0,
      },
      genes: [{
        symbol: 'COMT', variant: 'Val158Met', rsid: 'rs4680',
        genotype: 'A/G', status: 'monitor', evidenceTier: 'E3',
        studyCount: 5, description: 'Intermediate.',
        actionCount: 0, categories: ['mood'], pathway: 'Empty',
      }],
    }]

    const md = mentalHealthToMarkdown(sections, { COMT: [] })
    expect(md).not.toContain('### Actions for COMT')
  })
})

describe('mentalHealthToMarkdown — empty sections', () => {
  it('produces header-only output with no sections', () => {
    const md = mentalHealthToMarkdown([], {})
    expect(md).toContain('# Mental Health Genetic Profile')
    expect(md).toContain('2026-01-15')
    expect(md).toContain('Generated by Genome Toolkit')
    // No pathway headings
    expect(md).not.toContain('## ')
  })
})
