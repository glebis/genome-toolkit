import { describe, it, expect, vi, beforeEach } from 'vitest'
import {
  mentalHealthToMarkdown,
  pgxToMarkdown,
  riskLandscapeToMarkdown,
  addictionToMarkdown,
  geneToMarkdown,
  checklistToMarkdown,
} from '../lib/export'
import type { PathwaySection, ActionData } from '../types/genomics'
import type { PGxEnzymeSection } from '../types/pgx'
import type { ChecklistItem } from '../hooks/useChecklist'

// Freeze date so footer is deterministic
beforeEach(() => {
  vi.useFakeTimers()
  vi.setSystemTime(new Date('2026-01-15'))
})

// ── mentalHealthToMarkdown ──────────────────────────────────────────────────

describe('mentalHealthToMarkdown', () => {
  const sections: PathwaySection[] = [{
    narrative: {
      pathway: 'Methylation',
      status: 'actionable',
      body: 'Reduced folate conversion detected.',
      priority: '1 actionable',
      hint: '',
      geneCount: 1,
      actionCount: 1,
    },
    genes: [{
      symbol: 'MTHFR', variant: 'C677T', rsid: 'rs1801133',
      genotype: 'T/T', status: 'actionable', evidenceTier: 'E2',
      studyCount: 12, description: 'Reduced folate conversion.',
      actionCount: 1, categories: ['mood'], pathway: 'Methylation',
    }],
  }]

  const actions: Record<string, ActionData[]> = {
    MTHFR: [{
      id: 'a1', type: 'consider', title: 'Take methylfolate',
      description: 'Bypasses MTHFR.', evidenceTier: 'E2',
      studyCount: 12, tags: ['supplement'], geneSymbol: 'MTHFR', done: false,
    }],
  }

  it('includes header and date', () => {
    const md = mentalHealthToMarkdown(sections, actions)
    expect(md).toContain('# Mental Health Genetic Profile')
    expect(md).toContain('2026-01-15')
  })

  it('includes pathway heading and narrative', () => {
    const md = mentalHealthToMarkdown(sections, actions)
    expect(md).toContain('## Methylation')
    expect(md).toContain('Reduced folate conversion detected.')
  })

  it('renders gene table with correct columns', () => {
    const md = mentalHealthToMarkdown(sections, actions)
    expect(md).toContain('| MTHFR | C677T | T/T | actionable | E2 (12 sources) |')
  })

  it('renders actions as checklist items', () => {
    const md = mentalHealthToMarkdown(sections, actions)
    expect(md).toContain('- [ ] **CONSIDER**: Take methylfolate')
  })

  it('marks done actions with [x]', () => {
    const doneActions = { MTHFR: [{ ...actions.MTHFR[0], done: true }] }
    const md = mentalHealthToMarkdown(sections, doneActions)
    expect(md).toContain('- [x] **CONSIDER**: Take methylfolate')
  })

  it('includes footer', () => {
    const md = mentalHealthToMarkdown(sections, actions)
    expect(md).toContain('Generated by Genome Toolkit')
  })
})

// ── pgxToMarkdown ───────────────────────────────────────────────────────────

describe('pgxToMarkdown', () => {
  const sections: PGxEnzymeSection[] = [{
    enzyme: {
      symbol: 'CYP2D6', alleles: '*1/*4', status: 'intermediate',
      position: 30, description: 'Reduced metabolism.',
      guideline: 'CPIC', geneType: 'enzyme',
    },
    drugs: [{
      drugClass: 'SSRIs', impact: 'adjust', statusText: 'May need dose adjustment',
      description: 'Lower starting dose recommended.', drugList: 'fluoxetine, paroxetine',
      category: 'prescription',
    }],
  }]

  it('includes header and disclaimer', () => {
    const md = pgxToMarkdown(sections)
    expect(md).toContain('# PGx / Drug Metabolism Report')
    expect(md).toContain('Discuss all changes with your prescribing physician')
  })

  it('renders enzyme section with status', () => {
    const md = pgxToMarkdown(sections)
    expect(md).toContain('## CYP2D6 — INTERMEDIATE metabolizer')
    expect(md).toContain('**Genotype:** *1/*4')
    expect(md).toContain('**Guideline:** CPIC')
  })

  it('renders drug cards with impact marker', () => {
    const md = pgxToMarkdown(sections)
    expect(md).toContain('### ! SSRIs [ADJUST]')
    expect(md).toContain('**Drugs affected:** fluoxetine, paroxetine')
  })

  it('renders danger notes as warnings', () => {
    const dangerSections: PGxEnzymeSection[] = [{
      ...sections[0],
      drugs: [{
        ...sections[0].drugs[0],
        impact: 'danger',
        dangerNote: 'Avoid codeine completely.',
      }],
    }]
    const md = pgxToMarkdown(dangerSections)
    expect(md).toContain('### !!! SSRIs [DANGER]')
    expect(md).toContain('> **WARNING:** Avoid codeine completely.')
  })
})

// ── riskLandscapeToMarkdown ─────────────────────────────────────────────────

describe('riskLandscapeToMarkdown', () => {
  const causes = [{
    rank: 1, cause: 'Heart Disease', pct: 23.0,
    populationBarPct: 100, personalBarPct: 80,
    status: 'actionable' as const, genesText: 'APOE, MTHFR',
    statusText: 'Actionable',
    narrative: 'Elevated risk due to APOE variant.',
    genes: [{ symbol: 'APOE', variant: 'E3/E4', evidenceTier: 'E1', status: 'actionable' as const, description: 'Increased LDL.' }],
    actions: [{ type: 'consider' as const, text: 'Statins discussion' }],
  }]

  it('renders table with all causes', () => {
    const md = riskLandscapeToMarkdown(causes)
    expect(md).toContain('| 1 | Heart Disease | 23% | actionable | APOE, MTHFR |')
  })

  it('expands causes with genes and actions', () => {
    const md = riskLandscapeToMarkdown(causes)
    expect(md).toContain('## 1. Heart Disease')
    expect(md).toContain('**APOE** (E3/E4)')
    expect(md).toContain('- [ ] **CONSIDER**: Statins discussion')
  })

  it('skips expansion for causes without genes', () => {
    const noCauses = [{ ...causes[0], genes: undefined, actions: undefined }]
    const md = riskLandscapeToMarkdown(noCauses)
    expect(md).not.toContain('## 1. Heart Disease')
  })
})

// ── geneToMarkdown ──────────────────────────────────────────────────────────

describe('geneToMarkdown', () => {
  const gene = {
    symbol: 'COMT', variant: 'Val158Met', rsid: 'rs4680',
    genotype: 'A/G', status: 'monitor', evidenceTier: 'E2',
    studyCount: 30, description: 'Intermediate COMT activity.',
  }
  const actions: ActionData[] = [{
    id: 'c1', type: 'monitor', title: 'Track stress response',
    description: 'Journal for 2 weeks.', evidenceTier: 'E2',
    studyCount: 10, tags: ['lifestyle'], geneSymbol: 'COMT', done: false,
  }]

  it('renders gene header fields', () => {
    const md = geneToMarkdown(gene, actions)
    expect(md).toContain('# Gene Report: COMT')
    expect(md).toContain('**Variant:** Val158Met')
    expect(md).toContain('**Genotype:** A/G')
  })

  it('renders actions section', () => {
    const md = geneToMarkdown(gene, actions)
    expect(md).toContain('- [ ] **MONITOR**: Track stress response')
  })

  it('skips actions heading when none exist', () => {
    const md = geneToMarkdown(gene, [])
    expect(md).not.toContain('## Actions')
  })
})

// ── checklistToMarkdown ─────────────────────────────────────────────────────

describe('checklistToMarkdown', () => {
  const items: ChecklistItem[] = [
    { id: '1', gene_symbol: 'MTHFR', action_type: 'consider', title: 'Take methylfolate', done: false, done_at: null, notes: null, practical_category: '', health_domain: '', created_at: '' },
    { id: '2', gene_symbol: 'custom', action_type: 'monitor', title: 'Daily meditation', done: true, done_at: '2026-01-10', notes: null, practical_category: '', health_domain: '', created_at: '' },
  ]

  it('separates pending and done sections', () => {
    const md = checklistToMarkdown(items)
    expect(md).toContain('## Pending')
    expect(md).toContain('## Done')
  })

  it('renders pending items with gene symbol', () => {
    const md = checklistToMarkdown(items)
    expect(md).toContain('- [ ] Take methylfolate (MTHFR)')
  })

  it('renders done items without gene for custom', () => {
    const md = checklistToMarkdown(items)
    expect(md).toContain('- [x] Daily meditation')
    expect(md).not.toContain('Daily meditation (custom)')
  })

  it('skips pending section when all done', () => {
    const allDone = items.map(i => ({ ...i, done: true }))
    const md = checklistToMarkdown(allDone)
    expect(md).not.toContain('## Pending')
  })
})

